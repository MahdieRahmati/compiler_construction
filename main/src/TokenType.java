public enum 	TokenType {

	WHITESPACE,
	TAB,
	NEWLINE,

	PLUS,
	MINUS,
	MULTIPLY,
	DIVIDE,
    REMAINDER,

	AND,
	OR,
	NOT,
	TRUE,
	FALSE,
	
	EQUAL,
	ASSIGN,
	NOTEQUAL,
	GREATER,
	LOWER,
    GE,
    LE,

	INTEGER,
	FLOAT,
    STRING,
    BOOL,

	IF,
	ELSE,
	ELIF,

    LOOP,
    WHILE,

    BREAK,
    CONTINUE,

	SEMICOLON,
	COMMA,

	IDENTIFIER,

    PRINT,

	OPENPARANTHESIS,
	CLOSEPARANTHESIS,
	OPENCURLYBRACKET,
	CLOSECURLYBRACKET;



	public boolean isAuxiliary() {
		return this == NEWLINE || this == TAB
				|| this == WHITESPACE;
	}
}